CREATE TABLE `tbl_company` (
  `item_id` int(11) NOT NULL,
  `short_description` text NOT NULL,
  `phone` varchar(20) DEFAULT NULL,
  `email` varchar(64) DEFAULT NULL,
  `nip` varchar(10) DEFAULT NULL,
  `regon` varchar(9) DEFAULT NULL,
  `krs` varchar(10) DEFAULT NULL,
  `street` varchar(100) DEFAULT NULL,
  `city` varchar(100) DEFAULT NULL,
  `province` varchar(100) DEFAULT NULL,
  `postcode` varchar(10) DEFAULT NULL,
  `map_lat` float(9,6) DEFAULT NULL,
  `map_lng` float(10,6) DEFAULT NULL,
  `map_zoom` smallint(6) NOT NULL DEFAULT '10',
  `street_view_embed` text,
  `street_view_active` tinyint(1) NOT NULL DEFAULT '1',
  `payment_type` text,
  `hide_email` tinyint(1) NOT NULL DEFAULT '0',
  `allow_verification` tinyint(1) NOT NULL DEFAULT '1',
  `allow_comment` tinyint(1) NOT NULL DEFAULT '1',
  `delivery_type` text,
  PRIMARY KEY (`item_id`),
  CONSTRAINT `tbl_company_ibfk_1` FOREIGN KEY (`item_id`) REFERENCES `tbl_item` (`id`) ON DELETE CASCADE ON UPDATE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8