CREATE TABLE `tbl_item` (
  `id` int(11) NOT NULL AUTO_INCREMENT,
  `active` tinyint(1) NOT NULL DEFAULT '0',
  `cache_type` varchar(1) NOT NULL,
  `buy` tinyint(1) NOT NULL DEFAULT '0',
  `sell` tinyint(1) NOT NULL DEFAULT '0',
  `cache_package_id` int(11) NOT NULL DEFAULT '0',
  `category_id` int(11) NOT NULL DEFAULT '0',
  `name` varchar(64) NOT NULL,
  `user_id` int(11) NOT NULL DEFAULT '0',
  `alias` varchar(64) DEFAULT NULL,
  `date` date DEFAULT NULL,
  `thumbnail_file_id` int(11) DEFAULT NULL,
  `description` text,
  `old_company_id` int(11) DEFAULT NULL,
  `www` varchar(100) DEFAULT NULL,
  `youtube` varchar(11) DEFAULT NULL,
  `color` varchar(6) NOT NULL DEFAULT 'bbbbbb',
  `view_count` int(11) NOT NULL DEFAULT '0',
  PRIMARY KEY (`id`,`active`,`cache_type`,`buy`,`sell`,`cache_package_id`,`category_id`,`user_id`,`name`),
  UNIQUE KEY `id` (`id`),
  UNIQUE KEY `alias` (`alias`),
  KEY `date` (`date`),
  KEY `user_id` (`user_id`),
  KEY `category_id` (`category_id`),
  KEY `thumbnail_file_id` (`thumbnail_file_id`),
  KEY `cache_package_id` (`cache_package_id`),
  CONSTRAINT `tbl_item_ibfk_2` FOREIGN KEY (`user_id`) REFERENCES `tbl_user` (`id`) ON DELETE CASCADE ON UPDATE CASCADE,
  CONSTRAINT `tbl_item_ibfk_3` FOREIGN KEY (`category_id`) REFERENCES `tbl_category` (`id`) ON UPDATE CASCADE,
  CONSTRAINT `tbl_item_ibfk_4` FOREIGN KEY (`thumbnail_file_id`) REFERENCES `tbl_file` (`id`) ON DELETE SET NULL ON UPDATE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8