CREATE TABLE `tbl_user` (
  `id` int(11) NOT NULL AUTO_INCREMENT,
  `username` varchar(64) NOT NULL,
  `email` varchar(64) NOT NULL DEFAULT '',
  `password` varchar(64) NOT NULL,
  `remember_me` tinyint(1) NOT NULL DEFAULT '0',
  `active` tinyint(1) NOT NULL DEFAULT '1',
  `forename` varchar(32) DEFAULT NULL,
  `surname` varchar(64) DEFAULT NULL,
  `profile_picture` varchar(128) DEFAULT NULL,
  `type` int(2) NOT NULL DEFAULT '1',
  `ban` tinyint(1) NOT NULL DEFAULT '0',
  `website` varchar(128) DEFAULT NULL,
  `country` varchar(3) CHARACTER SET latin1 COLLATE latin1_general_ci NOT NULL DEFAULT 'pl',
  `timezone` int(1) NOT NULL DEFAULT '0',
  `language` varchar(3) CHARACTER SET latin1 COLLATE latin1_general_ci NOT NULL DEFAULT 'pl',
  `iban` varchar(36) CHARACTER SET latin1 COLLATE latin1_general_ci DEFAULT NULL,
  `born` date DEFAULT NULL,
  `registered` datetime DEFAULT NULL,
  `recovery_code` varchar(16) DEFAULT NULL,
  `verified` tinyint(1) NOT NULL DEFAULT '0',
  `verification_code` varchar(16) CHARACTER SET latin1 COLLATE latin1_general_ci DEFAULT NULL,
  `package_id` int(10) unsigned DEFAULT '1',
  `package_expire` date DEFAULT NULL,
  `expire_days_msg` int(11) NOT NULL DEFAULT '0',
  `show_email` tinyint(1) NOT NULL DEFAULT '0',
  `register_source` tinyint(1) NOT NULL DEFAULT '1',
  `facebook_id` varchar(50) DEFAULT NULL,
  `google_id` varchar(50) DEFAULT NULL,
  `thumbnail_file_id` int(11) DEFAULT NULL,
  `creators_tou_accepted` tinyint(1) NOT NULL DEFAULT '0',
  PRIMARY KEY (`id`,`username`,`email`),
  UNIQUE KEY `email` (`email`),
  UNIQUE KEY `username` (`username`),
  UNIQUE KEY `id` (`id`),
  KEY `active` (`active`),
  KEY `package_id` (`package_id`),
  KEY `thumbnail_file_id` (`thumbnail_file_id`),
  KEY `creators_tou_accepted` (`creators_tou_accepted`),
  CONSTRAINT `tbl_user_ibfk_1` FOREIGN KEY (`package_id`) REFERENCES `tbl_package` (`id`) ON DELETE SET NULL ON UPDATE CASCADE,
  CONSTRAINT `tbl_user_ibfk_2` FOREIGN KEY (`package_id`) REFERENCES `tbl_package` (`id`) ON DELETE SET NULL ON UPDATE CASCADE,
  CONSTRAINT `tbl_user_ibfk_3` FOREIGN KEY (`thumbnail_file_id`) REFERENCES `tbl_file` (`id`) ON DELETE SET NULL ON UPDATE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8